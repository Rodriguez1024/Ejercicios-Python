#Una cancha de futbol fue ampliada en un 20%. Determinar el área total en metros de la cancha.
longitudinicial = int(input("Ingresa la longitud inicial de la cancha en metros: "))
areainicial = longitudinicial 
area = areainicial * 0.20
areatotal = areainicial + area
print("El área total de la cancha es",areatotal)